void Mapa::mueve()
{
	fruta.mueve(0.025f);

	Moneda* auxmon = monedas.colision(fruta);
	if (auxmon != 0)
	{
		ETSIDI::play("sonidos/moneda.mp3");
		monedas.eliminar(auxmon);
		score.setScore (0, 0, 0, 7);
	}
	cuadraditos.colision(fruta);
	plataformas.colision(fruta);

	if (pinchos.colision(fruta))
	{
		Vida.setVidas(Vida.getVidas() - 1);
		fruta.setdeath(true);
	}

	if (enemigos.colision(fruta))
	{
		Vida.setVidas(Vida.getVidas() - 1);
		fruta.setdeath(true);
	}
	

	if (disparos.colision(fruta))
	{
		Vida.setVidas(Vida.getVidas() - 1);
		fruta.setdeath(true);
	}

	PowerUp* auxPU = PowerUps.colision(fruta);
	if (auxPU != 0)
	{
		ETSIDI::play("sonidos/powerup.mp3");
		score.setScore(0, 0, 1, 0);
		int a = lanzaDado(3, 1);
		if (a==fruta.getTipo())
		{
			a += 1;
		}
		Tpowerup = true;
		fruta.setTipo(a);
		fruta.dibuja();
		PowerUps.eliminar(auxPU);

		fruta.setVel(0, fruta.getVel().y);
	}

	if (interaccion::vacio(fruta))
	{
		Vida.setVidas(Vida.getVidas()-1);
		fruta.setdeath(true) ;
	}
	
	if (vida1 != 0)
	{
		if (interaccion::colision(fruta, vida1))
		{
			delete(vida1);

			ETSIDI::play("sonidos/1up.mp3");

			if (Vida.getVidas() <= 4)
			{
				Vida.setVidas(Vida.getVidas() + 1);
			}
		}
	}
	
	if (vida2  != 0)
	{
		if (interaccion::colision(fruta, vida2))
		{
			ETSIDI::play("sonidos/1up.mp3");

			delete(vida2);
			if (Vida.getVidas() <= 4)
			{

				Vida.setVidas(Vida.getVidas() + 1);
			}

		}
	}
	enemigos.colision(fruta);
	enemigos.mueve(0.025);
	disparos.eliminar(fruta);

	for (int i = 0; i < enemigos.getNumero(); i++)
	{
		Enemigo* Aux5 = enemigos[i];
		float dist = (Aux5->getPos() - fruta.getPos()).modulo();
		if (Aux5->getPos().x > fruta.getPos().x)
		{
			Aux5->setderecha(false);
		}
		else
		{
			Aux5->setderecha(true);
		}

		if (dist < 10)
		{
			if (dist < 9)
			{
				visibles.agregar(new EnemigoV(Aux5->getPos().x, Aux5->getPos().y, Aux5->getnumer()));
			}
				for (int j = 0; j < visibles.getNumero(); j++)
				{
					if (dist < 9)
					{
						
						EnemigoV* Auxe1 = visibles.enemigo(j);
						if (Auxe1->getNum() == 10)
						{
							Disparo10* d = new Disparo10(Auxe1->getPos().x, Auxe1->getPos().y);

							disparos.agregar(d);
							

							if (fruta.getPos().x < Auxe1->getPos().x)
							{
								d->setVel(-5, 0);
							
							}
							else
							{
								d->setVel(5, 0);
								
							}

							
						}
						if (Auxe1->getNum() == 11)
						{
							Disparo11* d = new Disparo11(Auxe1->getPos().x, Auxe1->getPos().y, fruta.getPos().x, fruta.getPos().y);

							disparos.agregar(d);

							

							if (fruta.getPos().x < Auxe1->getPos().x)
							{
								d->setVel(-5, 5);
								
							}
							else
							{
								d->setVel(5, 5);
								
							}

							
						}
						if (Auxe1->getNum() == 12)
						{
							Disparo12* d = new Disparo12(Auxe1->getPos().x, Auxe1->getPos().y);

							disparos.agregar(d);
							

							if (fruta.getPos().x < Auxe1->getPos().x)
							{
								d->setVel(-7.5, 0);
							}
							else
							{
								d->setVel(7.5, 0);
							}
							
						}
						
					}
					else if (dist >= 9 ) 
					{
						visibles.elimina(j);
					}
					}
		}
	}
	for (int i = 0; i < disparos.getNumero(); i++)
	{
		if (cuadraditos.colision(disparos[i]))
		{
			disparos.chocar(disparos[i], fruta);

		}

		if (enemigos.eliminar(*disparos[i]))
		{
			score.setScore(0, 0, 0, 2);
			visibles.eliminar(*disparos[i]);
			disparos.eliminar(disparos[i]);
			fruta.setDisparos(false);
		}

	}

	disparos.mueve(0.025);
	bandera.mueve();

	if (fruta.getPos().x >= 8 && fruta.getPos().x <=300)
	{
		ojo.x = fruta.getPos().x;
		posiocioncamara.x = fruta.getPos().x;
	}
	

	if (fruta.getPos().y >= -14 && fruta.getPos().y <= -6)
	{
		ojo.y = fruta.getPos().y;
		posiocioncamara.y = fruta.getPos().y;
	}

	
}

Vector Mapa::getPosOjo()
{
	return ojo;
}

void Mapa::teclamovimiento(unsigned char key)
{
	switch (key)
	{
	case ('d'):
		{
		if (fruta.getAc().x==-15)
		{
			break;
		}
		fruta.setAc(-20, fruta.getAc().y);
		
			break;
		}
	case ('a'):
		{
		if (fruta.getAc().x==15)
		{
			break;
		}
		fruta.setAc(20, fruta.getAc().y);
			break;
		}
	}
}


void Mapa::inicializamapa()
{
	vid = 1;
	for (int j = 0; j < largo; j++)
	{
		for (int i = 0; i < altura; i++)
		{
			switch (mapa[i][j])
			{
			case (30):
			{
				monedas.agregar(new Moneda(j, -i));
				break;
			}
			case(2):
			{
				cuadraditos.agregar(new cubo(j, -i));
				break;
			}
			case (3):
			{
				plataformas.agregar(new Plataforma(j, -i));
				break;
			}
			case (4):
			{
				cuadraditos.agregar(new cubo(j, -i));
				break;
			}
			case (5):
			{
				pinchos.agregar(new Pincho(j, -i));
				break;
			}
			case (7):
			{
				bandera.setPos(j, -i);
				break;
			}
			case(10):
			{
				enemigos.agregar(new Enemigo10(j, -i));
				break;
			}
			case(11):
			{
				enemigos.agregar(new Enemigo11(j, -i));
				break;
			}
			case(12):
			{
				enemigos.agregar(new Enemigo12(j, -i));
				break;
			}
			case (20):
			{
				PowerUps.agregar(new PowerUp(j, -i));
				break;
			}
			case (21):
			{
				if (vid == 1)
				{
					vida1 = new Vidas(j, -i);
					vid++;
				}
				else if (vid == 2)
				{
					vida2 = new Vidas(j, -i);
				}
				break;
			}
			}
		}
	}
}


