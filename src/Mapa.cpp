void Mapa::fondos(const char* fondo)
{
	for (int a = 0; a < 2; a++)
	{
		int posicionxdrch, posicionxizq;
		posicionxdrch = 180 * a +160;
		posicionxizq = 180 * a - 20;
	
		glEnable(GL_TEXTURE_2D);
		
		glBindTexture(GL_TEXTURE_2D, ETSIDI::getTexture(fondo).id);
		glDisable(GL_LIGHTING);
		glBegin(GL_POLYGON);
		glColor3f(1, 1, 1);
		glTexCoord2d(0, 1);    glVertex3f(posicionxizq, -28, -20);
		glTexCoord2d(1, 1);    glVertex3f(posicionxdrch, -28, -20);
		glTexCoord2d(1, 0);    glVertex3f(posicionxdrch, 8, -20);
		glTexCoord2d(0, 0);    glVertex3f(posicionxizq, 8, -20);
		glEnd();
		glEnable(GL_LIGHTING);
		glDisable(GL_TEXTURE_2D);
	}
}


void Mapa::teclado(unsigned char key)
{
	switch (key)
	{
	case('j'):
	{
		ojo.x -= 1;
		posiocioncamara.x -= 1;
		break;
	}
	case('l'):
	{
		ojo.x += 1;
		posiocioncamara.x += 1;
		break;
	}
	case('k'):
	{
		ojo.y -= 1;
		posiocioncamara.y -= 1;
		break;
	}
	case('i'):
	{
		ojo.y += 1;
		posiocioncamara.y += 1;
		break;
	}
	case ('+'):
	{
		posicionz -= 1;
		break;
	}
	case ('-'):
	{
		posicionz += 1;
		break;
	}
	case ('w'):
	{
		fruta.setVel(fruta.getVel().x, 20);
		break;
	}
	/*case ('s'):
	{

	}*/
	case ('a'):
	{
		fruta.setVel(-5, fruta.getVel().y);

		break;
	}
	case ('d'):
	{

		fruta.setVel(5, fruta.getVel().y);
		break;
	}

	}
}

